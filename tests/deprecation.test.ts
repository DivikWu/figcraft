/**
 * Deprecation & removal mechanism tests.
 *
 * Tests the Schema_Compiler deprecation/removal logic and runtime patterns:
 * 1. Deprecated tool description prefix injection
 * 2. Runtime deprecation warning injection into responses
 * 3. Removed tool migration guidance error responses
 * 4. Registry structure (GENERATED_DEPRECATED_TOOLS, GENERATED_REMOVED_TOOLS)
 * 5. Schema_Compiler removed tools detection logic
 *
 * Validates: Requirements 12.1
 */
import { describe, it, expect } from 'vitest';
import fc from 'fast-check';
import {
  GENERATED_DEPRECATED_TOOLS,
  GENERATED_REMOVED_TOOLS,
  GENERATED_ENDPOINT_REPLACES,
  GENERATED_ENDPOINT_METHOD_ACCESS,
} from '../packages/core-mcp/src/tools/_registry.js';

// ─── Helpers: simulate the patterns generated by compile-schema.ts ───

/**
 * Simulates the Schema_Compiler's deprecated description prefix logic.
 * When a tool has `deprecated: true` and `replaced_by`, the compiler prepends
 * `[DEPRECATED] Use {replaced_by} instead.` to the description.
 */
function applyDeprecatedPrefix(description: string, deprecated: boolean, replacedBy?: string): string {
  if (deprecated && replacedBy) {
    return `[DEPRECATED] Use ${replacedBy} instead. ${description}`;
  }
  return description;
}

/**
 * Simulates the runtime deprecation warning injection pattern.
 * The Schema_Compiler generates this code in bridge tool handlers for deprecated tools.
 */
function injectDeprecation(result: unknown, replacedBy: string): unknown {
  if (typeof result === 'object' && result !== null) {
    (result as Record<string, unknown>)._deprecation = {
      warning: `This tool is deprecated. Use ${replacedBy} instead.`,
      replacement: replacedBy,
    };
  }
  return result;
}

/**
 * Simulates the migration guidance error response for Phase 3 removed tools.
 */
function buildMigrationError(toolName: string, info: { endpoint: string; method: string }): object {
  return {
    ok: false,
    error: `Tool "${toolName}" has been removed. Use ${info.endpoint}(method: "${info.method}") instead.`,
    migration: { endpoint: info.endpoint, method: info.method },
  };
}

// ─── 1. Schema_Compiler deprecated tool description prefix ───

describe('Schema_Compiler deprecated description prefix', () => {
  it('prepends [DEPRECATED] prefix when deprecated=true and replaced_by is set', () => {
    const original = 'Get detailed node info by ID.';
    const result = applyDeprecatedPrefix(original, true, 'nodes.get');
    expect(result).toBe('[DEPRECATED] Use nodes.get instead. Get detailed node info by ID.');
  });

  it('starts with [DEPRECATED]', () => {
    const result = applyDeprecatedPrefix('Some description', true, 'text.create');
    expect(result.startsWith('[DEPRECATED]')).toBe(true);
  });

  it('contains the replaced_by value in the prefix', () => {
    const result = applyDeprecatedPrefix('Original desc', true, 'shapes.create_frame');
    expect(result).toContain('shapes.create_frame');
  });

  it('does not modify description when deprecated=false', () => {
    const original = 'Normal tool description.';
    const result = applyDeprecatedPrefix(original, false, 'nodes.get');
    expect(result).toBe(original);
  });

  it('does not modify description when replaced_by is undefined', () => {
    const original = 'Normal tool description.';
    const result = applyDeprecatedPrefix(original, true, undefined);
    expect(result).toBe(original);
  });
});

// ─── 2. Deprecation warning injection logic ───

describe('runtime deprecation warning injection', () => {
  it('injects _deprecation field into object results', () => {
    const result = { ok: true, data: 'some data' };
    injectDeprecation(result, 'nodes.get');
    expect(result).toHaveProperty('_deprecation');
  });

  it('_deprecation.warning contains the replacement info', () => {
    const result = { ok: true } as Record<string, unknown>;
    injectDeprecation(result, 'nodes.get');
    const dep = result._deprecation as { warning: string; replacement: string };
    expect(dep.warning).toBe('This tool is deprecated. Use nodes.get instead.');
  });

  it('_deprecation.replacement matches the replaced_by value', () => {
    const result = {} as Record<string, unknown>;
    injectDeprecation(result, 'components.list');
    const dep = result._deprecation as { warning: string; replacement: string };
    expect(dep.replacement).toBe('components.list');
  });

  it('does not modify null result', () => {
    const result = injectDeprecation(null, 'nodes.get');
    expect(result).toBeNull();
  });

  it('does not modify non-object result (string)', () => {
    const result = injectDeprecation('plain string', 'nodes.get');
    expect(result).toBe('plain string');
  });

  it('does not modify non-object result (number)', () => {
    const result = injectDeprecation(42, 'nodes.get');
    expect(result).toBe(42);
  });

  it('does not modify undefined result', () => {
    const result = injectDeprecation(undefined, 'nodes.get');
    expect(result).toBeUndefined();
  });
});

// ─── 3. Removed tools migration guidance ───

describe('removed tools migration guidance', () => {
  it('error message contains the tool name', () => {
    const err = buildMigrationError('get_node_info', { endpoint: 'nodes', method: 'get' });
    expect((err as Record<string, unknown>).error).toContain('get_node_info');
  });

  it('error message contains the endpoint name and method', () => {
    const err = buildMigrationError('get_node_info', { endpoint: 'nodes', method: 'get' });
    const error = (err as Record<string, unknown>).error as string;
    expect(error).toContain('nodes');
    expect(error).toContain('"get"');
  });

  it('migration object has correct endpoint and method', () => {
    const err = buildMigrationError('patch_nodes', { endpoint: 'nodes', method: 'update' }) as Record<string, unknown>;
    const migration = err.migration as { endpoint: string; method: string };
    expect(migration.endpoint).toBe('nodes');
    expect(migration.method).toBe('update');
  });

  it('ok field is false', () => {
    const err = buildMigrationError('delete_nodes', { endpoint: 'nodes', method: 'delete' }) as Record<string, unknown>;
    expect(err.ok).toBe(false);
  });

  it('produces correct output for various tool/endpoint/method combinations', () => {
    const cases = [
      { tool: 'create_text', endpoint: 'text', method: 'create' },
      { tool: 'set_text_content', endpoint: 'text', method: 'set_content' },
      { tool: 'create_frame', endpoint: 'shapes', method: 'create_frame' },
      { tool: 'list_components', endpoint: 'components', method: 'list' },
    ];
    for (const { tool, endpoint, method } of cases) {
      const err = buildMigrationError(tool, { endpoint, method }) as Record<string, unknown>;
      expect(err.ok).toBe(false);
      expect((err.error as string)).toContain(tool);
      expect((err.error as string)).toContain(endpoint);
      expect((err.error as string)).toContain(`"${method}"`);
      const migration = err.migration as { endpoint: string; method: string };
      expect(migration.endpoint).toBe(endpoint);
      expect(migration.method).toBe(method);
    }
  });
});

