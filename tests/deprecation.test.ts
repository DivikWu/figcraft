// ─── 5. Schema_Compiler removed tools detection logic ───

describe('Schema_Compiler removed tools detection logic', () => {
  it('GENERATED_ENDPOINT_REPLACES maps endpoints to flat tool names', () => {
    // Verify the mapping structure is correct — these are the tools that WOULD
    // populate GENERATED_REMOVED_TOOLS if they were removed from toolDefs
    for (const [endpoint, flatTools] of Object.entries(GENERATED_ENDPOINT_REPLACES)) {
      expect(Array.isArray(flatTools)).toBe(true);
      expect(flatTools.length).toBeGreaterThan(0);
      // Each flat tool name should be a non-empty string
      for (const tool of flatTools) {
        expect(typeof tool).toBe('string');
        expect(tool.length).toBeGreaterThan(0);
      }
    }
  });

  it('ENDPOINT_REPLACES flat tools have corresponding methods in ENDPOINT_METHOD_ACCESS', () => {
    // For each endpoint, verify that the number of replaced flat tools matches
    // the number of methods in ENDPOINT_METHOD_ACCESS
    for (const [endpoint, flatTools] of Object.entries(GENERATED_ENDPOINT_REPLACES)) {
      const methodAccess = GENERATED_ENDPOINT_METHOD_ACCESS[endpoint];
      expect(methodAccess).toBeDefined();
      const methodCount = Object.keys(methodAccess).length;
      // The number of replaced flat tools should equal the number of methods
      expect(flatTools.length).toBe(methodCount);
    }
  });

  it('simulated removal: if a flat tool is removed, it would appear in REMOVED_TOOLS with correct endpoint/method', () => {
    // Simulate the Schema_Compiler logic: for each endpoint, if a maps_to tool
    // is NOT in toolDefs, it should appear in GENERATED_REMOVED_TOOLS
    // We verify the mapping would be correct by checking ENDPOINT_REPLACES against ENDPOINT_METHOD_ACCESS
    for (const [endpoint, flatTools] of Object.entries(GENERATED_ENDPOINT_REPLACES)) {
      const methods = GENERATED_ENDPOINT_METHOD_ACCESS[endpoint];
      // Build a reverse map: flat tool name → method name
      const methodEntries = Object.entries(methods);
      // Each flat tool should correspond to exactly one method
      expect(flatTools.length).toBe(methodEntries.length);
    }
  });
});


// ─── Property 13: 废弃工具标记与警告 (Property-Based Test) ───

describe('Feature: endpoint-mode-refactor, Property 13: 废弃工具标记与警告', () => {
  /**
   * Validates: Requirements 10.1, 10.2, 10.3
   *
   * For any tool marked as deprecated: true:
   * (a) Its description should start with [DEPRECATED] prefix
   * (b) Calling the tool should produce a response with _deprecation field containing replaced_by info
   */
  it('applyDeprecatedPrefix always starts with [DEPRECATED] and contains replacedBy and original description', () => {
    fc.assert(
      fc.property(
        fc.string({ minLength: 1, maxLength: 50 }),  // toolName
        fc.string({ minLength: 1, maxLength: 100 }), // description
        fc.string({ minLength: 1, maxLength: 30 }),   // replacedBy (e.g. "nodes.get")
        (toolName, description, replacedBy) => {
          // Test description prefix
          const prefixed = applyDeprecatedPrefix(description, true, replacedBy);
          expect(prefixed.startsWith('[DEPRECATED]')).toBe(true);
          expect(prefixed).toContain(replacedBy);
          expect(prefixed).toContain(description);

          // Test deprecation injection
          const result: Record<string, unknown> = { ok: true, data: toolName };
          injectDeprecation(result, replacedBy);
          expect(result._deprecation).toBeDefined();
          expect((result._deprecation as { warning: string; replacement: string }).warning).toContain(replacedBy);
          expect((result._deprecation as { warning: string; replacement: string }).replacement).toBe(replacedBy);
        }
      ),
      { numRuns: 100 }
    );
  });
});

// ─── Property 14: 已移除工具的迁移指引 (Property-Based Test) ───

describe('Feature: endpoint-mode-refactor, Property 14: 已移除工具的迁移指引', () => {
  /**
   * Validates: Requirements 11.3
   *
   * For any removed flat tool name, the migration guidance error should contain
   * the correct endpoint tool name and method name.
   */

  // Build all possible (flatTool, endpoint, method) triples from registry data
  const REMOVAL_CANDIDATES: Array<{ flatTool: string; endpoint: string; method: string }> = [];
  for (const [endpoint, flatTools] of Object.entries(GENERATED_ENDPOINT_REPLACES)) {
    const methods = Object.keys(GENERATED_ENDPOINT_METHOD_ACCESS[endpoint] ?? {});
    for (const flatTool of flatTools) {
      for (const method of methods) {
        REMOVAL_CANDIDATES.push({ flatTool, endpoint, method });
      }
    }
  }

  it('buildMigrationError produces correct error with endpoint and method for all removal candidates', () => {
    fc.assert(
      fc.property(
        fc.constantFrom(...REMOVAL_CANDIDATES),
        ({ flatTool, endpoint, method }) => {
          const err = buildMigrationError(flatTool, { endpoint, method }) as {
            ok: boolean;
            error: string;
            migration: { endpoint: string; method: string };
          };
          expect(err.ok).toBe(false);
          expect(err.error).toContain(flatTool);
          expect(err.error).toContain(endpoint);
          expect(err.error).toContain(`"${method}"`);
          expect(err.migration.endpoint).toBe(endpoint);
          expect(err.migration.method).toBe(method);
        }
      ),
      { numRuns: 100 }
    );
  });
});
